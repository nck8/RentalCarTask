package com.rentalcar.domain;

public class CarType {
	
	public String carType1; 
	public String carType2_doors;
	public String transmission; 
	public String fuel; 
	public String airCon;

	public String getCarType1() {
		return carType1;
	}
	public void setCarType1(String carType1) {
		this.carType1 = carType1;
	}
	public String getCarType2_doors() {
		return carType2_doors;
	}
	public void setCarType2_doors(String carType2_doors) {
		this.carType2_doors = carType2_doors;
	}
	public String getTransmission() {
		return transmission;
	}
	public void setTransmission(String transmission) {
		this.transmission = transmission;
	}
	public String getFuel() {
		return fuel;
	}
	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
	public String getAirCon() {
		return airCon;
	}
	public void setAirCon(String airCon) {
		this.airCon = airCon;
	} 
	
}
